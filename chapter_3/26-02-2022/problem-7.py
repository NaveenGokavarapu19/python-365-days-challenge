'''
Program to demonstrate the use of relational operators
'''

x = 10
y = 20

print(str(x) + " < " + str(y) + " = " + str(x<y))
print(str(x) + " == " + str(y) + " = " + str(x==y))
print(str(x) + " != " + str(y) + " = " + str(x!=y))
print(str(x) + " > " + str(y) + " = " + str(x>y))
print(str(x) + " >= " + str(y) + " = " + str(x<=y))
