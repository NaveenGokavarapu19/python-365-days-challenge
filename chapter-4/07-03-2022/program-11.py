'''
program to display day corresponding to week

'''

num = int(input("Enter any number between 1 to 7: "))
if(num==1):
    print("Sunday")
elif(num==2):
    print("Monday")
elif(num==3):
    print("Tuesday")
elif(num==4):
    print("Wednesday")
elif(num==5):
    print("Thrusday")
elif(num==6):
    print("Friday")
elif(num==7):
    print("Saturday")
else:
    print("Wrong input")
