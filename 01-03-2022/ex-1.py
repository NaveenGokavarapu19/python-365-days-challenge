'''
Program to demonstrate all arithmetic operations  on two integers
'''

num1 = input("Enter the first number: ")
num2 = input("Enter the second number: ")

add_res = num1 + num2
sub_res = num1 - num2
mul_res = num1 * num2
div_res = num1 / num2
floor_res = num1 // num2
mod_res = num1 % num2
exp_res = num1 ** num2

print("num1 + num2 = " + add_res)
print("num1 - num2 = " + sub_res)
print("num1 * num2 = " + mul_res)
print("num1 / num2 = " + div_res)
print("num1 // num2 = " + floor_res)
print("num1 % num2 = " + mod_res)
print("num1 ** num2 = " + exp_res)