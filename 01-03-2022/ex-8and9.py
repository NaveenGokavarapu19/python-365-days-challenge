# Type casting is a method to convert the variable data into certain type in order perform the operations required by the user.


# In implict conversion python automatically converts one data type into another automatically
# eg a to int

a = 7
print(type(a))

# In explict conversion  
# In explict conversion python requires user intervention to convert one data type to another
#type casting

# int variable

a = 5

n = float(a)

